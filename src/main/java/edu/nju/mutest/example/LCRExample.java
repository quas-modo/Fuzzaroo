package edu.nju.mutest.example;

public class LCRExample {
    private LCRExample() {}
    public static int binaryAnd (int a, int b){return a & b;}

    public static int binaryOr (int a, int b){return a | b;}

    public static int XOR (int a, int b){return a ^ b;}

    public static boolean And (boolean a, boolean b){return a && b;}

    public static boolean Or (boolean a, boolean b){return a || b;}
}
